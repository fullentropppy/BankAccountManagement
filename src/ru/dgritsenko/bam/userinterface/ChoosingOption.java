package ru.dgritsenko.bam.userinterface;

import java.text.MessageFormat;

public enum ChoosingOption {
    DEFAULT("номер действия"),
    ACCOUNT("номер счета в списке");

    private final String title;

    // -----------------------------------------------------------------------------------------------------------------
    // OVERRIDDEN
    // -----------------------------------------------------------------------------------------------------------------

    /**
     * Возвращает строковое представление опции.
     * @return title
     */
    @Override
    public String toString() {
        return getTitle();
    }

    // -----------------------------------------------------------------------------------------------------------------
    // CONSTRUCTORS
    // -----------------------------------------------------------------------------------------------------------------

    /**
     * Создает новую опцию.
     * @param title заголовок опции
     */
    ChoosingOption(String title) {
        this.title = MessageFormat.format("> введите {0}: ", title);
    }

    // -----------------------------------------------------------------------------------------------------------------
    // GETTERS
    // -----------------------------------------------------------------------------------------------------------------

    /**
     * Возвращает заголовок опции.
     * @return название
     */
    public String getTitle() {
        return title;
    }
}
